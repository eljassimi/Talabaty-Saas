export function findCityId(cityName: string | null | undefined): number | null {
    if (!cityName)
        return null;
    const normalized = cityName.toLowerCase().trim();
    if (cityAliases[normalized]) {
        return cityAliases[normalized];
    }
    if (cityMapping[normalized]) {
        return cityMapping[normalized];
    }
    for (const [alias, id] of Object.entries(cityAliases)) {
        if (alias.includes(normalized) || normalized.includes(alias)) {
            return id;
        }
    }
    for (const [key, id] of Object.entries(cityMapping)) {
        if (key.includes(normalized) || normalized.includes(key)) {
            return id;
        }
    }
    const cleaned = normalized
        .replace(/^(ville|city|town|village)$/i, '')
        .replace(/[-\s_]+/g, ' ')
        .replace(/[()]/g, '')
        .trim();
    for (const [alias, id] of Object.entries(cityAliases)) {
        const aliasCleaned = alias.replace(/[-\s_]+/g, ' ').trim();
        if (aliasCleaned === cleaned || aliasCleaned.includes(cleaned) || cleaned.includes(aliasCleaned)) {
            return id;
        }
    }
    for (const [key, id] of Object.entries(cityMapping)) {
        const keyCleaned = key.replace(/[-\s_]+/g, ' ').replace(/[()]/g, '').trim();
        if (keyCleaned === cleaned || keyCleaned.includes(cleaned) || cleaned.includes(keyCleaned)) {
            return id;
        }
    }
    return null;
}
export function getUniqueCitiesFromOrders(orders: Array<{
    city?: string | null;
}>): string[] {
    const cities = new Set<string>();
    orders.forEach(order => {
        if (order.city) {
            cities.add(order.city);
        }
    });
    return Array.from(cities);
}
